% -------------------- Description --------------------- %
%                                                        %
%           Update vorticity values on the walls         %
%                                                        %
% ---------------------- Content ----------------------- %
global L dx dy

inner_field = 1;

% ------------------------ BASE ------------------------ %
% ------------- A_1 B_1 :: 0 < x(i) < L ---------------- %
j = 1;                                      % y == 0
for i = Range( 1, round(L/dx) + 1 )
    w  (i, j) = (2/dy^2)*( psi(i, 2) - psi(i, 1) );
end

% ------------------------------------------------------ %
% ----------- H_1 G_1 :: 0 < x(i) < L/10 --------------- %
j = round( (L/20)/dy) + 1;                  % y == L/20
for i = Range( 1, round( (L/10)/dx ) - 2 )
    w  (i, j) = (2/dy^2)*( psi(i, j-1) - psi(i, j) );
end

% ------------------------------------------------------ %
% ----------- G_1-F_1 :: L/40 < y(j) < L/20 ------------ %
i = round( (L/10)/dx ) - 2;                 % x == 2L/20^-
for j = Range( round( (L/40)/dy)-1 , round( (2*L/40)/dy) + 1 )
    w  (i, j) = (2/dx^2)*( psi(i-1, j) - psi(i, j) );
end

% ------------------------------------------------------ %
% ---------- F_1 E_1-:: 2L/20 < x < 3L/20 -------------- %
j = round( (L/40)/dy ) - 1;                 % y == L/40^-
for i = Range( round( (2*L/20)/dx) - 1, round( (3*L/20)/dx)+1 )
    w  (i, j) = (2/dy^2)*( psi(i, j-1) - psi(i, j) );
end

% ------------------------------------------------------ %
% ------------- Side of compartment handle ------------- %
i = round( (3*L/20)/dx) + 1;
for j = Range( round( (L/40)/dy)-1, round( (L/40)/dy) + 2 )
    w  (i, j) = (2/dx^2)*( psi(i+1, j) - psi(i, j) );
end

% ------------------------------------------------------ %
% ---------- F_1 E_1+:: 2L/20 < x < 3L/20 -------------- %
j = round( (L/40)/dy ) + 2;  j_EF = j;      % y == L/40^+
for i = Range( round( (2*L/20)/dx) + 1, round( (3*L/20)/dx) )
    w  (i, j) = (2/dy^2)*( psi(i, j+1) - psi(i, j) );
end

% ------------------------------------------------------ %
% ----------- F_1+ D  :: L/40 < y < L/20 --------------- %
i = round( (L/10)/dx ) + 1;                 % x == 2L/20^+
for j = Range( j_EF + 1, round( (6*L/20)/dy) )
    w  (i, j) = (2/dx^2)*( psi(i+1, j) - psi(i, j) );
end